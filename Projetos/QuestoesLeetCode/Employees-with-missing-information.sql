CREATE TABLE EMPLOYEES
(
    EMPLOYEE_ID NUMBER (4),
    NAME VARCHAR2(30),
    PRIMARY KEY (EMPLOYEE_ID)
);

SELECT * FROM EMPLOYEES;

CREATE TABLE SALARIES
(
    EMPLOYEE_ID NUMBER(4),
    SALARY NUMBER (5),
    PRIMARY KEY (EMPLOYEE_ID)
);

SELECT * FROM SALARIES;

