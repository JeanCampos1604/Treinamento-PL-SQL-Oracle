SELECT E.EMPLOYEE_ID
  FROM EMPLOYEES E, SALARIES S
  WHERE E.EMPLOYEE_ID NOT IN (SELECT S.EMPLOYEE_ID FROM SALARIES);   
  
SELECT E.EMPLOYEE_ID, S.EMPLOYEE_ID
  FROM EMPLOYEES E, SALARIES S
  WHERE E.EMPLOYEE_ID <> S.EMPLOYEE_ID;

SELECT
     CASE
        WHEN E.EMPLOYEE_ID IS NULL THEN S.EMPLOYEE_ID
        ELSE E.EMPLOYEE_ID
     END AS employee_id
  FROM EMPLOYEES E FULL OUTER JOIN SALARIES S
   ON E.EMPLOYEE_ID = S.EMPLOYEE_ID
   WHERE E.NAME IS NULL
   OR S.SALARY IS NULL
   ORDER BY S.EMPLOYEE_ID ASC;     