                                          2, 'Fora de Linha',
                                          3, 'Novo',
                                          4, 'Zerado') AS DESC_STATUS_DECODE,
       FSPRODUTOPARAMESTAB.CODESTOQUE, FSLOCALESTOQUE.DESCRICAO AS DESC_ESTOQUE
  FROM FSPRODUTO, FSPRODUTOPARAMESTAB, FSLOCALESTOQUE
 WHERE FSPRODUTOPARAMESTAB.CODPRODUTO = FSPRODUTO.CODPRODUTO 
   AND FSLOCALESTOQUE.CODESTOQUE = FSPRODUTOPARAMESTAB.CODESTOQUE
   AND FSPRODUTOPARAMESTAB.CODESTAB = 1
   AND FSPRODUTOPARAMESTAB.STATUS IN (1, 3)   
   AND EXISTS (SELECT 1
                 FROM FSSALDOESTOQUE
                WHERE FSSALDOESTOQUE.CODPRODUTO = FSPRODUTOPARAMESTAB.CODPRODUTO
                  AND FSSALDOESTOQUE.CODESTAB = FSPRODUTOPARAMESTAB.CODESTAB
                  AND FSSALDOESTOQUE.CODESTOQUE = FSPRODUTOPARAMESTAB.CODESTOQUE
                  AND (FSSALDOESTOQUE.ANTERIOR + FSSALDOESTOQUE.ENTRADA - FSSALDOESTOQUE.SAIDA) > 0)                                     
                  
