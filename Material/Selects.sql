SELECT FSPRODUTO.CODPRODUTO, FSPRODUTO.DESCRICAO, FSPRODUTOPARAMESTAB.CODESTAB, FSPRODUTOPARAMESTAB.STATUS,
       CASE
         WHEN (FSPRODUTOPARAMESTAB.STATUS = 0) THEN 'Inativo'
         WHEN (FSPRODUTOPARAMESTAB.STATUS = 1) THEN 'Ativo'
         WHEN (FSPRODUTOPARAMESTAB.STATUS = 2) THEN 'Fora de Linha' 
         WHEN (FSPRODUTOPARAMESTAB.STATUS = 3) THEN 'Novo' 
         WHEN (FSPRODUTOPARAMESTAB.STATUS = 4 ) THEN 'Zerado'        
       END AS DESC_STATUS_CASE,
       DECODE(FSPRODUTOPARAMESTAB.STATUS, 0, 'Inativo',
                                          1, 'Ativo',
                                          2, 'Fora de Linha',
                                          3, 'Novo',
                                          4, 'Zerado') AS DESC_STATUS_DECODE,
       FSPRODUTOPARAMESTAB.CODESTOQUE, FSLOCALESTOQUE.DESCRICAO AS DESC_ESTOQUE
  FROM FSPRODUTO, FSPRODUTOPARAMESTAB, FSLOCALESTOQUE
 WHERE FSPRODUTOPARAMESTAB.CODPRODUTO = FSPRODUTO.CODPRODUTO --join
   AND FSLOCALESTOQUE.CODESTOQUE (+)= FSPRODUTOPARAMESTAB.CODESTOQUE --left join
   AND FSPRODUTOPARAMESTAB.CODESTAB = 1
   AND FSPRODUTOPARAMESTAB.STATUS IN (1, 3)

