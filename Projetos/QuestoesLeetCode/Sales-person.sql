CREATE TABLE SalesPerson(
SALES_ID  INTEGER PRIMARY KEY, 
NAME VARCHAR(30),
SALARY INTEGER,    
COMMISSION_RATE INTEGER,    
HIRE_DATE DATE
);

CREATE TABLE COMPANY (
    COM_ID INTEGER PRIMARY KEY,
    NAME VARCHAR(30),
    CITY VARCHAR(30)
);

CREATE TABLE ORDERS (
    ORDER_ID INTEGER PRIMARY KEY,
    ORDER_DATE DATE,
    COM_ID INTEGER,
    SALES_ID INTEGER,
    AMOUNT INTEGER
);


